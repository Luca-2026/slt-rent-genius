// Helper functions
export function getAreasForLocation(locationId: string): LocalArea[] {
  return localAreas.filter((area) => area.locationId === locationId);
}

export function getAreaBySlug(slug: string): LocalArea | undefined {
  return localAreas.find((area) => area.slug === slug);
}

export function getAllAreaSlugs(): string[] {
  return localAreas.map((area) => area.slug);
}
